function addition(a, b) {
  return a + b;
}
function subtraction(a, b) {
  return a - b;
}
function multiplication(a, b) {
  return a * b;
}
function division(a, b) {
  return a / b;
}

console.log(addition(7, 1));
console.log(subtraction(9, 1));
console.log(multiplication(4, 2));
console.log(division(32, 4));
